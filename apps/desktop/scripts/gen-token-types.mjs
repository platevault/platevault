#!/usr/bin/env node
// Parses src/styles/tokens.css for `--pv-*` custom-property declarations and
// emits src/styles/tokens.d.ts exporting a sorted TS union of token names.
//
// Deterministic: token names are deduped and sorted so re-running produces a
// stable diff regardless of declaration order across `:root` / `[data-theme]`
// / density blocks in tokens.css.
//
// Usage: node scripts/gen-token-types.mjs
// Wired as the `tokens:types` package script.

import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import path from 'node:path';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const SRC = path.resolve(__dirname, '../src/styles/tokens.css');
const OUT = path.resolve(__dirname, '../src/styles/tokens.d.ts');

const css = readFileSync(SRC, 'utf8');

// Match `--pv-xyz:` declarations (custom property name up to the colon).
const tokenPattern = /--(pv-[a-z0-9-]+)\s*:/g;
const names = new Set();
let match;
while ((match = tokenPattern.exec(css)) !== null) {
  names.add(match[1]);
}

if (names.size === 0) {
  throw new Error(`No --pv-* custom properties found in ${SRC}`);
}

const sorted = [...names].sort((a, b) => a.localeCompare(b));

const banner = `// AUTO-GENERATED by scripts/gen-token-types.mjs — do not edit by hand.
// Run \`pnpm tokens:types\` to regenerate after editing tokens.css.
`;

const unionBody = sorted.map((name) => `  | '${name}'`).join('\n');

const contents = `${banner}
/** Every \`--pv-*\` design token declared in tokens.css. */
export type PvTokenName =
${unionBody};

/** A \`var(--pv-...)\` reference built from a known token name. */
export type PvTokenVar = \`var(--\${PvTokenName})\`;
`;

writeFileSync(OUT, contents, 'utf8');
// eslint-disable-next-line no-console
console.log(`Wrote ${sorted.length} token names to ${path.relative(process.cwd(), OUT)}`);
