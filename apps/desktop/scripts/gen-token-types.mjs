// NO SHEBANG — deliberately, unlike the sibling scripts in this folder.
// This is the only script here that is also IMPORTED as a module (by
// src/styles/tokens.types-drift.test.ts). Under vitest on Windows a leading
// `#!` reaches the transform unstripped and the suite dies with
// `SyntaxError: Invalid or unexpected token` before a single test runs; the
// same import is fine on Linux and macOS, so CI only reddens on one platform.
// Nothing needs the shebang: the file is mode 644 and is invoked as
// `node scripts/gen-token-types.mjs` via the `tokens:types` package script.
//
// Parses src/styles/tokens.css and packages/tokens/tokens-docs.css (handoff
// 04 — shared spacing/type-scale/radius/motion/font tokens) for `--pv-*`
// custom-property declarations and emits src/styles/tokens.d.ts exporting a
// sorted TS union of token names.
//
// Deterministic: token names are deduped and sorted so re-running produces a
// stable diff regardless of declaration order across `:root` / `[data-theme]`
// / density blocks, or across the two source files.
//
// Usage: node scripts/gen-token-types.mjs
// Wired as the `tokens:types` package script.
//
// `extractTokenNames`/`renderTokenTypesDts` are exported so
// src/styles/tokens.types-drift.test.ts can run the same generation logic
// against tokens.css and assert it matches the committed tokens.d.ts,
// without duplicating the parsing rules as a second source of truth.

import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath, pathToFileURL } from 'node:url';
import path from 'node:path';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
export const SRC = path.resolve(__dirname, '../src/styles/tokens.css');
export const FOUNDATION_SRC = path.resolve(__dirname, '../../../packages/tokens/tokens-docs.css');
export const OUT = path.resolve(__dirname, '../src/styles/tokens.d.ts');

/**
 * Extracts every `--pv-*` custom-property name declared across the given CSS
 * source strings, deduped and sorted so the result is stable regardless of
 * declaration order across `:root` / `[data-theme]` / density blocks, or
 * across source files.
 */
export function extractTokenNames(cssTexts) {
  // Character class deliberately wider than the kebab-case convention: a
  // narrower `[a-z0-9-]` silently DROPS any token containing an uppercase
  // letter or underscore, so such a token would ship in the CSS while being
  // absent from the type — a hole that fails open. Naming is enforced
  // elsewhere; this extractor's job is to miss nothing.
  const tokenPattern = /--(pv-[A-Za-z0-9_-]+)\s*:/g;
  const names = new Set();
  for (const css of cssTexts) {
    let match;
    while ((match = tokenPattern.exec(css)) !== null) {
      names.add(match[1]);
    }
  }
  return [...names].sort((a, b) => a.localeCompare(b));
}

/** Renders the tokens.d.ts file contents for a sorted, deduped token-name list. */
export function renderTokenTypesDts(sortedNames) {
  const banner = `// AUTO-GENERATED by scripts/gen-token-types.mjs — do not edit by hand.
// Run \`pnpm tokens:types\` to regenerate after editing tokens.css.
`;

  const unionBody = sortedNames.map((name) => `  | '${name}'`).join('\n');

  return `${banner}
/** Every \`--pv-*\` design token declared in tokens.css or foundation.css. */
export type PvTokenName =
${unionBody};

/** A \`var(--pv-...)\` reference built from a known token name. */
export type PvTokenVar = \`var(--\${PvTokenName})\`;
`;
}

function main() {
  const css = [readFileSync(SRC, 'utf8'), readFileSync(FOUNDATION_SRC, 'utf8')];
  const sorted = extractTokenNames(css);

  if (sorted.length === 0) {
    throw new Error(`No --pv-* custom properties found in ${SRC} or ${FOUNDATION_SRC}`);
  }

  writeFileSync(OUT, renderTokenTypesDts(sorted), 'utf8');
  // eslint-disable-next-line no-console
  console.log(`Wrote ${sorted.length} token names to ${path.relative(process.cwd(), OUT)}`);
}

// Only run generation when executed directly (`node scripts/gen-token-types.mjs`
// or `pnpm tokens:types`) — not when imported by the drift-guard test.
//
// Compared via pathToFileURL rather than a `file://` template: on Windows
// process.argv[1] is a backslashed drive path (`C:\...\gen-token-types.mjs`),
// so interpolating it yields `file://C:\...` which never equals import.meta.url
// (`file:///C:/.../gen-token-types.mjs`) — `pnpm tokens:types` would silently
// generate nothing there.
if (import.meta.url === pathToFileURL(process.argv[1]).href) {
  main();
}
