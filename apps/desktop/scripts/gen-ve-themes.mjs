#!/usr/bin/env node
// gen-ve-themes.mjs — generate vanilla-extract theme contract + themes from DTCG sources.
//
// Reads tokens/ (same as build-tokens.mjs) and emits src/styles/themes.css.ts containing:
//   - createThemeContract() for all token keys
//   - createGlobalTheme('[data-theme=X]', ...) per theme
//   - Foundation tokens as a separate globalStyle :root block
//
// Usage: node apps/desktop/scripts/gen-ve-themes.mjs [--check]

import { readFileSync, writeFileSync, readdirSync } from 'node:fs';
import { join, basename, resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const COMPONENT_TOKEN_DIR = join(resolve(dirname(fileURLToPath(import.meta.url)), '..'), 'tokens', 'component');

const APP_DIR = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const TOKENS_DIR = join(APP_DIR, 'tokens');
const OUT = join(APP_DIR, 'src/styles/themes.css.ts');

// Component token files provide layout/dimension tokens (control heights,
// sidebar widths, row height, etc.) that are theme-invariant like foundation.
const componentFiles = readdirSync(COMPONENT_TOKEN_DIR)
  .filter((f) => f.endsWith('.json'))
  .sort();
const componentEntries = componentFiles.flatMap((f) =>
  Object.entries(JSON.parse(readFileSync(join(COMPONENT_TOKEN_DIR, f), 'utf8'))),
);

const DEFAULT_THEME = 'warm-slate';

// ── Load DTCG sources ─────────────────────────────────────────────────────────

const foundation = JSON.parse(readFileSync(join(TOKENS_DIR, 'foundation.json'), 'utf8'));
const semantic = JSON.parse(readFileSync(join(TOKENS_DIR, 'semantic.json'), 'utf8'));

const themeIds = readdirSync(join(TOKENS_DIR, 'themes'))
  .filter((f) => f.endsWith('.json'))
  .map((f) => basename(f, '.json'))
  .sort();

const themes = Object.fromEntries(
  themeIds.map((id) => [
    id,
    JSON.parse(readFileSync(join(TOKENS_DIR, 'themes', `${id}.json`), 'utf8')),
  ]),
);

// ── Helpers ───────────────────────────────────────────────────────────────────

function toCamel(id) {
  return id.replace(/-([a-z])/g, (_, c) => c.toUpperCase());
}

// Sanitize token key to valid JS identifier for the contract object.
function toJsKey(key) {
  return key.replace(/-([a-z0-9])/g, (_, c) => c.toUpperCase());
}

// Resolve DTCG alias references against a theme's raw palette.
function resolveValue(val, themeTokens) {
  if (typeof val !== 'string') return String(val);
  return val.replace(/\{([^}]+)\}/g, (_, ref) => {
    const resolved = themeTokens[ref];
    if (resolved && resolved.$value != null) {
      return resolveValue(String(resolved.$value), themeTokens);
    }
    return `var(--pv-${ref})`;
  });
}

// ── Collect all theme-varying token keys ──────────────────────────────────────

const allPaletteKeys = new Set();
for (const id of themeIds) {
  for (const key of Object.keys(themes[id])) {
    allPaletteKeys.add(key);
  }
}

// Semantic aliases that reference theme tokens.
const semanticKeys = Object.keys(semantic).filter((k) => !k.startsWith('$'));

// All keys in the theme contract (palette + semantic aliases).
const contractKeys = [...new Set([...allPaletteKeys, ...semanticKeys])].sort();

// ── Generate output ───────────────────────────────────────────────────────────

const lines = [];

lines.push("// AUTO-GENERATED by scripts/gen-ve-themes.mjs from apps/desktop/tokens/");
lines.push("// Do not edit by hand. Run `node apps/desktop/scripts/gen-ve-themes.mjs`");
lines.push("// after editing the DTCG sources. CI fails if this file drifts.");
lines.push("");
lines.push("import { createThemeContract, createGlobalTheme, globalStyle } from '@vanilla-extract/css';");
lines.push("");

// Theme contract — shape only, no values.
lines.push("export const vars = createThemeContract({");
for (const key of contractKeys) {
  lines.push(`  ${toJsKey(key)}: null,`);
}
lines.push("});");
lines.push("");

// Per-theme global themes.
for (const id of themeIds) {
  const camel = toCamel(id);
  const selector = id === DEFAULT_THEME
    ? `':root:not([data-theme]), [data-theme="${id}"]'`
    : `'[data-theme="${id}"]'`;

  lines.push(`createGlobalTheme(${selector}, vars, {`);
  for (const key of contractKeys) {
    const jsKey = toJsKey(key);
    // Resolve value: first check theme palette, then semantic, then skip.
    const themeDef = themes[id][key];
    const semDef = semantic[key];
    let val;
    if (themeDef) {
      val = resolveValue(String(themeDef.$value), themes[id]);
    } else if (semDef) {
      val = resolveValue(String(semDef.$value), themes[id]);
    } else {
      // Token not defined in this theme — fall back to default theme.
      const defaultDef = themes[DEFAULT_THEME][key];
      if (defaultDef) {
        val = resolveValue(String(defaultDef.$value), themes[DEFAULT_THEME]);
      } else {
        val = '';
      }
    }
    lines.push(`  ${jsKey}: ${JSON.stringify(val)},`);
  }
  lines.push("});");
  lines.push("");
}

// Foundation tokens (non-color, theme-invariant) as :root CSS variables.
const foundationEntries = Object.entries(foundation).filter(([, d]) => d.$type !== 'color');

// All theme-invariant tokens: foundation (non-color) + component dimension tokens.
// Emitted via createGlobalTheme so .css.ts files get type-safe refs (uvars.*).
const invariantEntries = [
  ...foundationEntries,
  ...componentEntries,
];

if (invariantEntries.length > 0) {
  lines.push("// Typed theme-invariant contract (spacing, type scale, radii, weights, layout dims).");
  lines.push("// Use uvars.* in .css.ts files instead of raw var(--pv-*) strings.");
  lines.push("export const uvars = createGlobalTheme(':root', {");
  for (const [key, def] of invariantEntries) {
    lines.push(`  ${toJsKey(key)}: ${JSON.stringify(String(def.$value))},`);
  }
  lines.push("});");
  lines.push("");
}

const output = lines.join('\n') + '\n';

// ── Write or check ───────────────────────────────────────────────────────────

if (process.argv.includes('--check')) {
  let existing = '';
  try {
    existing = readFileSync(OUT, 'utf8');
  } catch {
    console.error(`ERROR: ${OUT} does not exist. Run the generator first.`);
    process.exit(1);
  }
  if (existing.split('\r\n').join('\n') !== output.split('\r\n').join('\n')) {
    console.error(
      `ERROR: ${OUT} is out of date with apps/desktop/tokens/.\n` +
        'Run `node apps/desktop/scripts/gen-ve-themes.mjs` and commit the result.',
    );
    process.exit(1);
  }
  console.log('OK: themes.css.ts matches DTCG sources.');
} else {
  writeFileSync(OUT, output);
  console.log(`wrote ${OUT} (${themeIds.length} themes, ${contractKeys.length} contract keys)`);
}
